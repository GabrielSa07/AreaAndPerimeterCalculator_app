export const areaTriangle = (b, h) => {
    result = (b* h) / 2
    return result
}

export const areaRectangle = (b, h) => {
    result = b * h
    return result
}

export const areaCircle = (a) => {
    result = Math.PI * (a ** 2)
}

export const areaSquare = (a) => {
    result = a ** 2
    return result
}

export const areaTrapeze = (bme, bma, h) => {
    result = ((bme + bma) * h ) / 2 
    return result
}

export const areaDiamond = (dma, dme) => {
    result = (dma * dme) / 2
    return result
}



