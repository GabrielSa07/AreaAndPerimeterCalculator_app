const perimeterTriangle = (a, b, c) => {
    result = a + b + c
    return result
}

const perimeterRectangle = (a, b) => {
    result = (a * 2) + (b * 2)
    return result
}

const perimeterCircle = (a) => {
    result = (2 * Math.PI) * a
}

const perimeterSquare = (a) => {
    result = a * 4
    return result
}

const perimeterTrapeze = (a, b, c, d) => {
    result = a + b + c + d
    return result
}

const perimeterDiamond = (a) => {
    result = a * 4
    return result
}